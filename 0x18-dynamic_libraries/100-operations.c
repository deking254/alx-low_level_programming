/**
 * add - check the code 
 * @a: a
 * @b: b
 * Return: Always 0.
 */
int add(int a , int b)
{
return (a + b);
}

/**
 * sub - check the code 
 * @a: a
 * @b: b
 * Return: Always 0.
 */
int sub(int a , int b)
{
return (a - b);
}
/**
 * div - check the code
 * @a: a
 * @b: b
 * Return: Always 0.
 */
int div(int a , int b)
{
return (a / b);
}

/**
 * mul - check the code
 * @a: a
 * @b: b
 * Return: Always 0.
 */
int mul(int a , int b)
{
return (a * b);
}
/**
 * mod - check the code
 * @a: a
 * @b: b
 * Return: Always 0.
 */
int mod(int a , int b)
{
return (a % b);
}
